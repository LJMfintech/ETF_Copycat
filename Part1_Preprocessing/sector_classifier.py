import pandas as pd

def classify_etf_sectors(df):
    print("\n⏳ [2단계] ETF 이름 및 기초지수명 기반 섹터 분류를 시작합니다...")
    
    # 1. 테마/섹터 동의어 사전 (코드명 기준)
    sector_dict = {
        'IT': ['IT'],
        'AI': ['AI', '인공지능'],
        'AI전력': ['AI전력'],
        '소버린AI': ['소버린AI'],
        '로봇': ['로봇', '휴머노이드'],
        '반도체': ['반도체', '소부장'],
        '반도체공정': ['반도체핵심공정', '반도체전공정', '반도체후공정' ],
        '이차전지': ['이차전지', '2차전지'],
        '자동차': ['자동차', '모빌리티'],
        '방산': ['방산', '방위산업', '국방'],
        '조선': ['조선'],
        '증권': ['증권'],
        '원자력': ['원자력'],
        '원자력SMR': ['원자력SMR'],
        '화장품': ['화장품', 'K-뷰티'],
        '네트워크': ['네트워크'],
        'esg': ['esg', 'ESG'],
        '메타버스': ['메타버스'],
        '여행레저': ['여행레저', '여행', '레저'],
        '건설': ['건설'],
        '게임': ['게임'],
        '에너지': ['에너지'],
        '에너지화학': ['에너지화학'],
        '철강': ['철강'],
        '기후변화': ['기후변화', '탄소효율'],
        '음식': ['음식', '식음료'],
        '통신': ['통신', '5G'],
        '스마트밸류': ['스마트밸류'],
        '뉴딜디지털': ['뉴딜디지털'],
        '밸류업': ['밸류업'],
        '삼성그룹': ['삼성그룹'],
        '설비투자': ['설비투자'],
        '금융': ['금융'],
        '헬스케어': ['바이오', '헬스'],
        '의료기기': ['의료기기'],
        'KPOP': ['KPOP', 'K-POP'],
        '엔터': ['엔터테인먼트', '엔터', '콘텐츠' ]
    }
    
    # 2. 시장대표 지수 사전 (ETF기초지수명 기준)
    market_dict = {
        '시장대표/ MSCI KOREA': ['MSCI Korea'],
        '시장대표/코스피': ['코스피지수', '코스피 TR'],
        '시장대표/코스피200': ['코스피 200', 'KOSPI 200', '코스피200', 'KOSPI200'],
        '시장대표/코스피100': ['코스피 100', 'KOSPI 100', '코스피100', 'KOSPI100'],
        '시장대표/코스닥150': ['코스닥 150', 'KOSDAQ 150', '코스닥150', 'KOSDAQ150'],
        '시장대표/코스닥글로벌': ['코스닥글로벌', '코스닥 글로벌'],
        '시장대표/KRX300': ['KRX 300', 'KRX300']
    }
    
    df['섹터분류'] = '기타/분류불가'
    
    # 3. 테마/섹터 매핑 (코드명 기준) - 우선순위 1
    for sector_name, keywords in sector_dict.items():
        pattern = '|'.join(keywords)
        mask = df['코드명'].str.contains(pattern, case=False, na=False)
        df.loc[mask, '섹터분류'] = sector_name
        
    # 4. 시장대표 지수 매핑 (ETF기초지수명 기준) - 우선순위 2
    # ✨수정된 부분: 이미 테마로 분류된 것은 덮어씌우지 않고, '기타/분류불가'인 것만 찾아냅니다.
    for market_name, keywords in market_dict.items():
        pattern = '|'.join(keywords)
        
        # 조건 1: 기초지수명에 해당 키워드가 포함되어 있고
        condition1 = df['ETF기초지수명'].str.contains(pattern, case=False, na=False)
        # 조건 2: 현재 '기타/분류불가' 상태인 항목
        condition2 = df['섹터분류'] == '기타/분류불가'
        
        # 두 조건을 모두 만족(&)하는 행만 시장대표 지수로 변경
        df.loc[condition1 & condition2, '섹터분류'] = market_name
        
    print("  ✅ 섹터 및 시장대표 지수 분류 완료 (기존 테마 보존)")
    
    return df